public class Declaraciones_Operadores {
    public static void main(String[] args) {

        int a=5;

        int b;

        b=7;  //defino las constantes

        int c=b+a;  //opero con las constantes, a partir de aqui a y b no cambiaran

        c+=6;  //uso un incremento de una de las constantes

        System.out.println(c);

        double d=5;  //a partir de aqui uso double para que al operar me saque los decimales

        double e;

        e=7;

        double f=e/d;  

        f--;

        System.out.println(f);

    }
}
